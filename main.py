# Primeiro Código Leigo em Python

# Aprendendo sobre Input:

# Unico problema do input é que ele retorna 
# os valores como string, é preciso fazer a conversão 
nome = input("Digite seu nome: ")
idade = int(input("Digite sua idade: ")) 
peso = float(input("Digite seu peso: "))

# Uma forma para saber o tipo da variável é usando o Type depois do print
# Ele vai retornar o tipo da variável
 
# Colocando o type ao entorno das variáveis idade e peso, para saber se está retornando corretamente.
print(nome)
print(idade)
print(peso)
print("---------------")
print(type(idade))
print(type(peso))
